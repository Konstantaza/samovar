class CustomAnnotator(BaseAnnotator):
    """Annotator class for user-defined custom classification tools.
    
    This class acts as a universal wrapper. It expects a bash script that 
    strictly follows a defined API using command-line flags (-i, -I, -d, -o, -p) 
    and outputs a 2-column TSV file (sequence_id, taxID).
    """

    @property
    def default_cmd(self) -> str:
        """Return the default command to invoke the custom script.
        
        This can be overridden in the run_config via the 'cmd' key.
        """
        return "bash src/annotators/custom.sh"

    def get_expected_outputs(self, sample: str, output_dir: str) -> List[str]:
        """Define the expected output file for the custom tool."""
        out_file = os.path.join(output_dir, f"{sample}_{self.run_name}.custom.out")
        return [out_file]

    def get_snakemake_shell_cmd(self, input_r1: str, input_r2: str, outputs: List[str]) -> str:
        """Construct the shell command using the standardized custom API flags."""
        out_file = outputs[0]
        
        cmd = (
            f"{self.cmd} "
            f"-i {input_r1} "
            f"-I {input_r2} "
            f"-d {self.db_path} "
            f"-o {out_file} "
            f"-p \"{self.extra}\""
        )
        return cmd

    def parse_output(self, file_path: str) -> pd.DataFrame:
        """Parse the output of the custom tool.
        
        Since the custom bash script is required to output a clean, 
        2-column TSV file (seq, taxID), the parsing is straightforward.
        """
        df = pd.read_table(file_path, header=None)
        
        # In case the custom script outputs more columns by accident,
        # we strictly take only the first two.
        df = df.iloc[:, [0, 1]]
        
        df.columns = ["seq", "taxID"]
        
        return df
        

def get_annotator_instance(tool_type: str, run_config: Dict, config: Dict) -> BaseAnnotator:
    """Factory function to instantiate the correct annotator class.
    
    Args:
        tool_type: A string identifying the tool (e.g., 'kraken2', 'custom').
        run_config: The dictionary containing specific run settings.
        config: The global configuration dictionary.
        
    Returns:
        An instantiated object of a subclass of BaseAnnotator.
    """
    # Normalize the string to avoid case/alias mismatches
    tool = tool_type.lower()
    
    if tool in ["kraken2"]:
        return Kraken2Annotator(run_config, config)
    elif tool in ["kaiju"]:
        return KaijuAnnotator(run_config, config)
    elif tool in ["metaphlan", "metaphlan4", "mpa", "mp4"]:
        return MetaPhlanAnnotator(run_config, config)
    elif tool in ["kraken", "kraken1"]:
        return Kraken1Annotator(run_config, config)
    elif tool in ["krakenuniq", "krakenu"]:
        return KrakenUniqAnnotator(run_config, config)
    elif tool in ["custom"]:
        return CustomAnnotator(run_config, config)
    else:
        raise ValueError(f"Unknown annotator type requested: {tool_type}")
        
    